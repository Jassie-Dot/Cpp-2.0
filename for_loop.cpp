#include<iostream>
using namespace std;
int main()
{
    int num;
    cout<<"How many times you want to print the message: "<<endl;
    cin>>num;
    for(int i=0; i<=num; i++)
    {
        cout<<"*************************************\n"<<"HAPPY BIRTHDAY TO YOU!\n"<<"*************************************"<<endl;
    }
    return 0;
}